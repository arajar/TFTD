//////////////////////////////////////////////////////////////////////////

bool Game::Init()
{
	Engine::Init();

	bool initialized = false;
	m_gameConfig = new GameConfig("config.json");
	if (m_gameConfig->Init())
	{
		Engine::Resize(m_gameConfig->GetWindowWidth(), m_gameConfig->GetWindowHeight());

		initialized = true;
	}

	return initialized;
}

